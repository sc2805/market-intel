// Pre-computed aggregate impacts
export function computeAggregateImpacts(stories) {
  const categories = ['markets', 'sectors', 'assets'];
  const result = { markets: {}, sectors: {}, assets: {} };

  categories.forEach(cat => {
    const keys = Object.keys(stories[0].impacts[cat]);
    keys.forEach(key => {
      result[cat][key] = stories.reduce((sum, s) => sum + s.impacts[cat][key], 0);
    });
  });

  return result;
}

// Compute total impact magnitude for ranking
export function getImpactMagnitude(story) {
  let total = 0;
  Object.values(story.impacts).forEach(category => {
    Object.values(category).forEach(score => {
      total += Math.abs(score);
    });
  });
  return total;
}

// Get sorted stories by impact magnitude
export function getStoriesByImpact(stories) {
  return [...stories].sort((a, b) => getImpactMagnitude(b) - getImpactMagnitude(a));
}

// Get top N stories
export function getTopStories(stories, n = 3) {
  return getStoriesByImpact(stories).slice(0, n);
}

// Net sentiment calculation
export function getNetSentiment(stories) {
  let totalScore = 0;
  let totalCells = 0;
  stories.forEach(story => {
    Object.values(story.impacts).forEach(category => {
      Object.values(category).forEach(score => {
        totalScore += score;
        totalCells++;
      });
    });
  });
  return {
    score: totalScore,
    average: (totalScore / totalCells).toFixed(2),
    label: totalScore < -10 ? 'RISK-OFF' : totalScore > 10 ? 'RISK-ON' : 'NEUTRAL',
    severity: totalScore < -30 ? 'severe' : totalScore < -10 ? 'moderate' : totalScore > 10 ? 'bullish' : 'mixed'
  };
}
