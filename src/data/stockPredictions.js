export function getConfidenceColor(confidence) {
  if (confidence >= 85) return '#30d158';
  if (confidence >= 70) return '#ffc832';
  if (confidence >= 55) return '#ff8f8f';
  return '#ff2d55';
}

export function getRecommendationColor(rec) {
  switch (rec) {
    case 'STRONG BUY': return '#30d158';
    case 'BUY': return '#5cd97a';
    case 'HOLD': return '#ffc832';
    case 'SELL': return '#ff2d55';
    case 'AVOID': return '#ff6b6b';
    default: return '#8888a0';
  }
}

